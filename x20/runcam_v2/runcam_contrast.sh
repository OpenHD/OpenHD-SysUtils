#!/bin/bash

/usr/local/bin/x20/runcam_v2/get_cam_i2c.sh
CAM=$?
BUS=0

if [ $1 -eq 1 ]; then
	i2cset -y $BUS $CAM 0x12 0x00 0x03 0x8C 0x1c 0x20 0x28 0x2c i
elif [ $1 -eq 2 ]; then
	i2cset -y $BUS $CAM 0x12 0x00 0x03 0x8C 0x36 0x38 0x3a 0x3c i
elif [ $1 -eq 3 ]; then
	i2cset -y $BUS $CAM 0x12 0x00 0x03 0x8C 0x46 0x48 0x4a 0x4c i
else
	echo "Valid values of contrast are: 1, 2, or 3"
fi

