#!/bin/bash

/usr/local/bin/x20/runcam_v2/get_cam_i2c.sh
CAM=$?
BUS=0

#RUNCAM_Read_Write(camera_device, 0x000040, 0x0022ffa9);
#RUNCAM_Read_Write(camera_device, 0x000040, 0x002effa9);

# Try 0x26 or 0x2A, original 0x22
if [ $1 -eq 0 ]; then
     i2cset -y $BUS $CAM 0x12 0x00 0x00 0x40 0x00 0x22 0xff 0xa9 i
elif [ $1 -eq 1 ]; then
     i2cset -y $BUS $CAM 0x12 0x00 0x00 0x40 0x00 0x2a 0xff 0xa9 i
elif [ $1 -eq 2 ]; then
     i2cset -y $BUS $CAM 0x12 0x00 0x00 0x40 0x00 0x26 0xff 0xa9 i
elif [ $1 -eq 3 ]; then
     i2cset -y $BUS $CAM 0x12 0x00 0x00 0x40 0x00 0x2e 0xff 0xa9 i
else
    echo "Valid values of flip are: 0, 1 2 or 3"
    echo "No rotation, hflip, vflip, both"
fi
